/*
*********************************************************************************************
*                                  GetMTV335ID
*
* Description:Get the ID of MTV335    
*
* Arguments  : none.
*
* Return     : the ID of MTV335.
*
* Note(s)    : none.
*********************************************************************************************
*/

int GetMTV335ID(void)
{
    int device_id;

    if(IICReadByte(MTV335_IIC_ADDR, REG_ID, &device_id, 0) < 0)
        return IIC_ERROR;

    device_id = 0x335;
    
    return device_id;

}

